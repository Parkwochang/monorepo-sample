interface BibleVerse {
  content: string;
  sheet: number;
  verse: number;
  volume: number;
}

// 성경 구절 데이터 1000개
export const bibleVerses: BibleVerse[] = Array.from({ length: 1000 }, (_, index): BibleVerse => {
  // 기본 구절들
  const baseVerses = [
    '태초에 하나님이 천지를 창조하시니라',
    '여호와께서 모세에게 말씀하여 이르시되',
    '하나님이 세상을 이처럼 사랑하사 독생자를 주셨으니',
    '두려워하지 말라 내가 너와 함께 함이라',
    '내가 너를 지명하여 불렀나니 너는 내 것이라',
    '너희는 먼저 그의 나라와 그의 의를 구하라',
    '평안을 너희에게 끼치노니 곧 나의 평안을 너희에게 주노라',
    '여호와는 나의 목자시니 내게 부족함이 없으리로다',
    '우리가 알거니와 하나님을 사랑하는 자 곧 그의 뜻대로 부르심을 입은 자들에게는 모든 것이 합력하여 선을 이루느니라',
    '인자가 온 것은 섬김을 받으려 함이 아니라 도리어 섬기려 하고',
  ];

  const verseIndex = index % baseVerses.length;
  const content = baseVerses[verseIndex];

  if (!content) {
    throw new Error(`Invalid verse index: ${verseIndex}`);
  }

  return {
    content,
    sheet: Math.floor(Math.random() * 150) + 1, // 1-150 사이의 장
    verse: Math.floor(Math.random() * 30) + 1, // 1-30 사이의 절
    volume: Math.floor(Math.random() * 66) + 1, // 1-66 사이의 권 (성경 전체 권수)
  };
});

